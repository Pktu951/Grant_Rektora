ANGLE_ERROR = 2
# dy dx wzgledem lokalnej
DX_AFTER_1_MESSAGE = TURN_RADIUS * 2 * math.sin(ANGLE_AFTER_1_MESSAGE / 2) * math.cos((math.pi - ANGLE_AFTER_1_MESSAGE) / 2)
DY_AFTER_1_MESSAGE = TURN_RADIUS * 2 * math.sin(ANGLE_AFTER_1_MESSAGE / 2) * math.sin((math.pi - ANGLE_AFTER_1_MESSAGE) / 2)

# Resolution
RESOLUTION_X = math.ceil(CANVAS_WIDTH * DISTANCE_1_MESSAGE)
