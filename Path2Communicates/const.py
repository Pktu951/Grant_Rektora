_TIME_CIRCUMFERENCE = _CIRCUMFERENCE / VELOCITY
_NUMBER_OF_MESSAGES_1_CIRCUMFERENCE = math.ceil(_TIME_CIRCUMFERENCE / MESSAGE_FREQUENCY)
